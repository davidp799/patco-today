@Composable
fun PatcoTodayTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable () -> Unit
) {
    val context = LocalContext.current
    val prefs = PreferenceManager.getDefaultSharedPreferences(context)

    // Get theme preference from settings (1=Light, 2=Dark, 3=Follow System)
    val themePreference = prefs.getString("device_theme", "3")?.toInt() ?: 3
    val dynamicColorPreference = prefs.getBoolean("dynamic_colors", false)

    // Determine dark theme based on preference
    val useDarkTheme = when (themePreference) {
        1 -> false // Light theme
        2 -> true  // Dark theme
        3 -> darkTheme // Follow system (use parameter)
        else -> darkTheme
    }

    val colorScheme = when {
        dynamicColorPreference && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            if (useDarkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }

        useDarkTheme -> DarkColorScheme
        else -> LightColorScheme
    }

    MaterialTheme(
        colorScheme = colorScheme,
        typography = Typography,
        content = content
    )
}